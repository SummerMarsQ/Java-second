package edu.step.calculator;

public class Calculator {

    public static int addition(int a, int b)
    {
        return a+b;
    }
    public static double addition(int a , double b)
    {
        return a+b;
    }
    public static double addition(double a , double b)
    {
        return a+b;
    }
    public static double addition(double a , int b)
    {
        return a+b;
    }

    public static int substraction(int a, int b)
    {
        return a-b;
    }
    public static double substraction(int a , double b)
    {
        return a-b;
    }
    public static double substraction(double a , double b)
    {
        return a-b;
    }
    public static double substraction(double a , int b)
    {
        return a-b;
    }

    public static int multiplication(int a, int b)
    {
        return a*b;
    }
    public static double multiplication(int a , double b)
    {
        return a*b;
    }
    public static double multiplication(double a , double b)
    {
        return a*b;
    }
    public static double multiplication(double a , int b)
    {
        return a*b;
    }

    public static double division(int a, int b)
    {
        return (double)a/b;
    }
    public static double division(int a , double b)
    {
        return a/b;
    }
    public static double division(double a , double b)
    {
        return a/b;
    }
    public static double division(double a , int b)
    {
        return a/b;
    }
}
